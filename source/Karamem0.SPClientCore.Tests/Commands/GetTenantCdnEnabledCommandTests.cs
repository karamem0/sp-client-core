using Karamem0.SharePoint.PowerShell.Tests.Utilities;
using NUnit.Framework;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Karamem0.SharePoint.PowerShell.Commands.Tests;

[Category("Karamem0.SharePoint.PowerShell.Commands")]
public class GetTenantCdnEnabledCommandTests
{

    [Test()]
    public void InvokeCommand_GetPublic_ShouldSucceed()
    {
        using var context = new PSCmdletContext();
        _ = context.Runspace.InvokeCommand(
            "Connect-KshSite",
            new Dictionary<string, object>()
            {
                ["Url"] = context.AppSettings["AdminUrl"],
                ["ClientId"] = context.AppSettings["ClientId"],
                ["CertificatePath"] = context.AppSettings["CertificatePath"],
                ["PrivateKeyPath"] = context.AppSettings["PrivateKeyPath"]
            }
        );
        var result1 = context.Runspace.InvokeCommand<bool>(
            "Get-KshTenantCdnEnabled",
            new Dictionary<string, object>()
            {
                ["Public"] = true
            }
        );
        var actual = result1.ElementAt(0);
        Assert.That(actual, Is.True);
    }

    [Test()]
    public void InvokeCommand_GetPrivate_ShouldSucceed()
    {
        using var context = new PSCmdletContext();
        _ = context.Runspace.InvokeCommand(
            "Connect-KshSite",
            new Dictionary<string, object>()
            {
                ["Url"] = context.AppSettings["AdminUrl"],
                ["ClientId"] = context.AppSettings["ClientId"],
                ["CertificatePath"] = context.AppSettings["CertificatePath"],
                ["PrivateKeyPath"] = context.AppSettings["PrivateKeyPath"]
            }
        );
        var result1 = context.Runspace.InvokeCommand<bool>(
            "Get-KshTenantCdnEnabled",
            new Dictionary<string, object>()
            {
                ["Private"] = true
            }
        );
        var actual = result1.ElementAt(0);
        Assert.That(actual, Is.True);
    }

}
